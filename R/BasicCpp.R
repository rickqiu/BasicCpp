#' @title  addBase
#' @description Add two vectors
#' @aliases addBase
#' @export addBase
#' @author Rick Qiu
#' @param x A vector
#' @param y A Vector
#' @return Addition of x and y
#' @examples
#' addBase(1:10, 1:10)
addBase <- function (x, y)
{
    x + y
}
